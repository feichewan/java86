package com.fy.demo;
/**
 * 表述一个人的信息【基础类型】
 * @author wzf
 * @date   2020年4月10日
 */

public class Demo2 {

	public static void main(String[] args) {
		// 个人信息
		//sysout快捷方式的快捷键ALT+/
		System.out.println("王正飞");//输出姓名字符串String
		System.out.println("25");//年龄:int类型，整型
		System.out.println("0");//月薪：double类型，小数点
		//Ctrl+Shift+f:格式化，把代码列对齐
		System.out.println("镇江丹阳");//住址：字符串String
		System.out.println("英雄联盟");//爱好：字符串String
		System.out.println("男");//性别：Char类型
		

	}

}
